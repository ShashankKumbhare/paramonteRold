corMat <- R6::R6Class(      "corMat",
                            
                            inherit = CorCovMat,
                            
                            # Public >> ############################################################################################
                            
                            public = list(
                                
                                initialize = function( dataFrame, # initialize >> ##################################################
                                                       columns       = NULL,
                                                       methodName    = "ParaMonte",
                                                       reportEnabled = TRUE,
                                                       method        = "pearson",
                                                       Err ) {
                                    
                                    self$method = method
                                    
                                    super$initialize( dataFrame     = dataFrame,
                                                      columns       = columns,
                                                      methodName    = methodName,
                                                      reportEnabled = reportEnabled,
                                                      Err           = Err )
                                    
                                }
                                
                            ) # << Public
                            
)
