getFuncName  <- function() {
    callingFun = as.list(sys.call(-1))[[1]]
    calledFun  = as.list(sys.call())[[1]]
    
    callingFun = as.character(callingFun)
    
    return(callingFun)
    # message(paste(callingFun, " is calling ", calledFun, sep=""))
}
