sourceFolder  <- function(folderName, recursive = FALSE, ...) {
    
    files <- list.files(folderName, pattern = "[.][rR]$", full.names = TRUE, recursive = recursive)
    if (!length(files))
        stop(simpleError(sprintf('No R files in folderName "%s"', folderName)))
    src <- invisible(lapply(files, source, ...))
    # message(sprintf('%s files sourced from folderName "%s"', length(src), folderName))
    
}
